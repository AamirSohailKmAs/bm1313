    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if ($request->id) {
            $request->validate([
                'name' => ['required', 'unique:movements,name,' . $request->id]
            ]);
            Movement::findOrFail($request->id)->update(['name' => $request->name]);
        } else {
            $request->validate([
                'name' => ['required', 'unique:movements,name']
            ]);

            Movement::create([
                'name' => $request->name,
            ]);
        }
        return redirect()->route('pos.dropdown')->with('success', __('Movements Added'));
    }

